 #calculadora
number_1 = int(input('primeiro numero: '))
number_2 = int(input('segundo numero: '))
operacao = input("operacao: ")

if operacao == '+':
    print(number_1 + number_2)

if operacao == '-':
    print(number_1 - number_2) 

if operacao == '/':
    print(number_1 / number_2)

if operacao == '*':
    print(number_1 * number_2) 